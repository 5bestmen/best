public class DisplayTemplateDataForm{

    private String id;
    private String index;
    private String icon;
    private String deviceTemplateData;
    private String query;
    private String del;
    private String deleteIds;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIndex() {
        return index;
    }

    public void setIndex(String index) {
        this.index = index;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getDeviceTemplateData() {
        return deviceTemplateData;
    }

    public void setDeviceTemplateData(String deviceTemplateData) {
        this.deviceTemplateData = deviceTemplateData;
    }

    public String getQuery() {
        return query;
    }

    public void setQuery(String query) {
        this.query = query;
    }

    public String getDel() {
        return del;
    }

    public void setDel(String del) {
        this.del = del;
    }

    public String getDeleteIds() {
        return deleteIds;
    }

    public void setDeleteIds(String deleteIds) {
        this.deleteIds = deleteIds;
    }
}
